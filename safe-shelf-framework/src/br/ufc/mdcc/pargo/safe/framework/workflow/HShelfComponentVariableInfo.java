package br.ufc.mdcc.pargo.safe.framework.workflow;

public class HShelfComponentVariableInfo {

	private String id;
	private String candidates;
	private String portURLs;
	private String deployedID;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCandidates() {
		return candidates;
	}
	public void setCandidates(String candidates) {
		this.candidates = candidates;
	}
	public String getPortURLs() {
		return portURLs;
	}
	public void setPortURLs(String portURLs) {
		this.portURLs = portURLs;
	}
	public String getDeployedID() {
		return deployedID;
	}
	public void setDeployedID(String deployedID) {
		this.deployedID = deployedID;
	}
	
	
}
